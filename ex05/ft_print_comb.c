#include <unistd.h>

void	print_num(char *a)
{
	write(1, &a[0], 1);
	write(1, &a[1], 1);
	write(1, &a[2], 1);
}

void	ft_print_comb(void)
{
	char a[3];

	a[0] = '0';
	a[1] = '1';
	a[2] = '2';
	while (a[0] <= '7')
	{
		a[1] = a[0] + 1;
		while (a[1] <= '8')
		{
			a[2] = a[1] + 1;
			while (a[2] <= '9')
			{
				print_num(a);
				if (!(a[0] == '7' && a[1] == '8' && a[2] == '9'))
					write(1, ", ", 2);
				a[2]++;
			}
			a[1]++;
		}
		a[0]++;
	}
}
