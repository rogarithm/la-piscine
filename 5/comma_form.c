#include <unistd.h>

void	ft_print_array_2(void)
{
	char ltr;

	ltr = 97;
	while (ltr <= 'z')
	{
		if (ltr < 'z')
		{
			write(1, &ltr, 1);
			ltr++;
			write(1, ", ", 2);
		}
		else if (ltr == 'z')
		{
			write(1, &ltr, 1);
			ltr++;
		}
	}
}
