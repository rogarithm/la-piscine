#include <unistd.h>

void	ft_print_comb2(void)
{
	char a[4];

	a[0] = '0';
	a[1] = '0';
	a[2] = '0';
	a[3] = '1';
	while (a[0] <= '9')
	{
		while (a[1] <= '9')
		{
			write(1, &a[0], 1);
			write(1, &a[1], 1);
			write(1, " ", 1);
			a[1]++;
		}
		a[0]++;
	}
	a[3] = a[1] + 1;
	while (a[2] <= '9')
	{
		while (a[3] <= '9')
		{
			write(1, &a[2], 1);
			write(1, &a[3], 1);
			write(1, ",", 1);
			write(1, " ", 1);
			a[3]++;
		}
		a[2]++;
	}
}
